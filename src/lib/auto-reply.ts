// ────────────────────────────────────────────────
// 主函数
// ────────────────────────────────────────────────
export function generateAutoReply(name: string, content: string): string | null {
  const text = (name + ' ' + content).toLowerCase()

  // 1. 名字特判（最高优先）
  for (const [key, replies] of Object.entries(NAME_REPLIES)) {
    if (name.includes(key)) {
      return pick(replies)
    }
  }

  // 2. 内容中点名大锤/锤子三号 → 专属回复
  if (content.includes('大锤') && !name.includes('大锤')) {
    return '大锤的事我记下了！他来的时候我帮你转达 🔨👀'
  }

  // 3. 关键词匹配，取最高优先级
  let bestRule: MatchRule | null = null
  for (const rule of RULES) {
    if (rule.keywords.some((kw) => text.includes(kw.toLowerCase()))) {
      if (!bestRule || rule.priority > bestRule.priority) {
        bestRule = rule
      }
    }
  }

  if (bestRule) {
    return pick(REPLY_POOL[bestRule.pool])
  }

  // 4. 随机兜底（70% 概率触发）
  if (Math.random() < 0.7) {
    return pick(REPLY_POOL.fallback)
  }

  return null
}
