/**
 * 随机获取一条碎碎念（SSR 友好：服务端用固定种子，客户端可以随机）
 */
export function getRandomMusing(seed?: number): string {
  const index = seed !== undefined
    ? seed % musings.length
    : Math.floor(Math.random() * musings.length)
  return musings[index]
}
